#Ternary Operator (Conditional Expression)

# is an operator that taked 3 arguments
# one-line if-else statment
#syntax is <result_if_true> ? <condition> else <result_if_false>

#traditional
stacks = 4
if stacks >= 3: 
    print("Coding Dojo")
else:
    print("You are not Coding Dojo")

#Using Ternary
(print("Coding Dojo") if stacks >= 3 else print("You are not Coding Dojo"))


(print("Hello World!!") if "a" == "a" else print("Good Evening"))