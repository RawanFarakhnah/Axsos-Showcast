#casting  Integers:
x = int(1) # x will be 1
x = int(1.1) #x will be 1
x = int("1") #x will be 1


#casting  Floats:
y = float(1) # y will be 1.0
y = float(1.1) #y will be 1.1
y = float("1") #y will be 1.0
y = float("1.1") #y will be 1.1

#casting  Strings:
s = str("s1") #s will be "s1"
s = str(1) # s will be "1"
s = str(1.1) #s will be "1.1"


