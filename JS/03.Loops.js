//Types of Loops ..
//There are two distinct kinds of loops that we will utlize: for and while loops
/**
 * While loops similar to for loops by executing as long as a specific condition remains true.
 * we can even structure a while loops to resemble a for loop if we prefer.
 */
console.log("\n Use For Loops");
for (var i = 0; i < 3; i++) {
  console.log(i);
}

console.log("\n Use While Loops");
var i = 0;
while (i < 3) {
  console.log(i);
  i++;
}

//for loops : executing as long as a specific condition remains true.

//while loops: similar to if statment that continues to run as long as the condition remains true
